package ödev9;

public class main {

	public static void main(String[] args) {
		int i=1;
		while(i<10) {
			System.out.println(i);
			i+=2;
		}
		System.out.println("while döngüsü bittii ");
		int j=1;
		do {
			System.out.println(j);
			j+=2;
			
		}while(j<10);
		System.out.println("while-do döngüsü bittii ");
	}

}
